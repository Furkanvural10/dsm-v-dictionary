import Foundation
import UIKit

struct Font {
    
    struct FontSize{
        static let fontSize14: UIFont = .systemFont(ofSize: 14)
        static let fontSize15: UIFont = .systemFont(ofSize: 15)
    }
    
    struct BoldFontSize {
        static let boldFont35: UIFont = .boldSystemFont(ofSize: 35)
        static let boldFont30: UIFont = .boldSystemFont(ofSize: 30)
        static let boldFont20: UIFont = .boldSystemFont(ofSize: 20)
    }
}
